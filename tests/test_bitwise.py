# 位运算测试
a = 10
b = 3

# 按位或
print("a | b =", a | b)  # 10 | 3 = 11

# 按位与
print("a & b =", a & b)  # 10 & 3 = 2

# 按位异或
print("a ^ b =", a ^ b)  # 10 ^ 3 = 9

# 按位取反
print("~a =", ~a)  # ~10 = -11

# 左移
print("a << 1 =", a << 1)  # 10 << 1 = 20

# 右移
print("a >> 1 =", a >> 1)  # 10 >> 1 = 5

# 组合运算
print("(a | b) & (a ^ b) =", (a | b) & (a ^ b))  # 9